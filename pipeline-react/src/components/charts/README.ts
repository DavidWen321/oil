/*
1. 能耗趋势分析 (EnergyTrendChart)
   业务需求: 时间序列能耗监控
   核心功能:
   - 折线图 + 面积图渐变
   - 峰值/谷值 Pin 标注
   - 平均线显示
   - 多泵站对比 (虚线)
   - 趋势箭头 (↑↓)
   - dataZoom 缩放

2. 管道压力分布 (PressureDistributionChart)
   业务需求: 管道节点压力监控
   核心功能:
   - 渐变色折线 (根据压力值变色)
   - 安全阈值线 (红色实线 7.5 MPa)
   - 警告阈值线 (橙色虚线 6.5 MPa)
   - 节点点击事件
   - 热力图模式 (可选)
   - 状态提示 (⚠️ 超过安全阈值)

3. 泵站运行状态 (PumpStationGauge)
   业务需求: 泵站效率和功率监控
   核心功能:
   - 双层仪表盘 (效率 + 功率)
   - 效率分段着色 (90%+ 绿, 75-90% 青, 60-75% 橙, <60% 红)
   - 状态颜色 (运行/停止/故障)
   - 多泵站雷达图对比 (PumpStationRadar)

4. 泵组合优化对比 (OptimizationComparisonChart)
   业务需求: 8 种泵组合方案对比
   核心功能:
   - 柱状图对比能耗
   - 折线图显示成本
   - 散点图模式 (能耗 vs 成本)
   - 最优方案高亮 (绿色 + Pin 标记)
   - 不可行方案灰显
   - 双 Y 轴 (能耗 + 成本)

5. 故障诊断因果图 (FaultDiagnosisSankey)
   业务需求: 故障类型、原因、概率分析
   核心功能:
   - 桑基图显示因果关系链路
   - 节点分类着色 (故障/原因/影响)
   - 流量粗细表示概率
   - 节点点击事件
   - 树图模式 (FaultDiagnosisTree) 备选
*/

